int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 1 && nb < 100000000)
	{
		while (i * i < nb && i < 10000)
			i++;
		if (i * i == nb)
			return (i);
	}
	if (nb >= 100000000)
	{
		i = 10000;
		while (i * i < nb && i < 46500)
			i++;
		if (i * i == nb)
			return (i);
	}
	if (nb == 1)
		return (1);
	else
		return (0);
}
